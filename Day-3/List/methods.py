list = [2,1,3]

# 1. APPEND METHOD
list.append(4) #adds one element at the end 
[2, 1, 3, 4] 

# SORT METHOD
list.sort( ) #sorts in ascending order A[3, 1, 2] 
[1, 2, 3] 

list.sort( reverse=True ) #sorts in decending order A[3, 1, 2]
[3,2,1]

list.reverse() #reverses list 
[3,1,2]

list.insert(1,5) #insert element at index (index ,element)


list.remove(1) #removes first occurrence of element 

list.pop(2) #removes paricular index