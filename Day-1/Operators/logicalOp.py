#Logical Operators

print(not True);
print(not False)

a = 50;
b = 30;

print(not(a>b))
print("OR operator : ", (a==b) or (a>b));
print("And operator : ", (a==b) and (a>b));


val1 = True;
val2 = False;

#Both should be true
print("And operator : ", val1 and val2);

val3 = True;
val4 = False;
# one of them should be true
print("OR operator : ", val3 or val4);

